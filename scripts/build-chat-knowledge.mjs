#!/usr/bin/env node
/**
 * Reads service page sources and generates chatKnowledge.generated.ts for the FAQ bot.
 * Run automatically before dev/build, or: npm run chat:knowledge
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const SERVICES_DIR = path.join(ROOT, 'src/app/services');
const OUT_FILE = path.join(ROOT, 'src/lib/chatKnowledge.generated.ts');

function slugFromPath(servicePath) {
  return servicePath.replace(/^\/services\//, '');
}

function extractMetadata(content) {
  const title = content.match(/title:\s*['"]([^'"]+)['"]/)?.[1] ?? '';
  const description =
    content.match(/description:\s*\n\s*['"]([^'"]+)['"]/)?.[1] ??
    content.match(/description:\s*['"]([^'"]+)['"]/)?.[1] ??
    '';
  const servicePath = content.match(/path:\s*['"]([^'"]+)['"]/)?.[1] ?? '';
  return { title, description, path: servicePath };
}

function extractIntro(content) {
  const match = content.match(/<ServiceWatermarkIntro>\s*<p>\s*([\s\S]*?)<\/p>/);
  if (!match) return '';
  return match[1]
    .replace(/\{[^}]+\}/g, '')
    .replace(/\s+/g, ' ')
    .trim()
    .slice(0, 420);
}

function extractListItems(content) {
  const items = [];
  const arrayRe = /const\s+[A-Z_]+\s*=\s*\[([\s\S]*?)\];/g;
  let m;
  while ((m = arrayRe.exec(content)) !== null) {
    const block = m[1];
    const strings = [...block.matchAll(/['"]([^'"]+)['"]/g)].map((s) => s[1].trim());
    items.push(...strings);
  }
  const unique = [...new Set(items)];
  return unique.slice(0, 8);
}

function buildServiceEntry(filePath) {
  const content = fs.readFileSync(filePath, 'utf8');
  const { title, description, path: servicePath } = extractMetadata(content);
  if (!title || !servicePath) return null;

  const intro = extractIntro(content);
  const listItems = extractListItems(content);

  const summary = description || intro.slice(0, 200);
  let details = '';
  if (intro && intro !== summary) {
    details = intro;
  }
  if (listItems.length) {
    const listText = `Incluye: ${listItems.join('; ')}.`;
    details = details ? `${details} ${listText}` : listText;
  }
  if (details.length > 600) details = `${details.slice(0, 597)}…`;

  return {
    name: title,
    path: servicePath,
    summary,
    details: details || undefined,
  };
}

function main() {
  const dirs = fs
    .readdirSync(SERVICES_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);

  const services = [];
  for (const dir of dirs.sort()) {
    const pageFile = path.join(SERVICES_DIR, dir, 'page.tsx');
    if (!fs.existsSync(pageFile)) continue;
    const entry = buildServiceEntry(pageFile);
    if (entry) services.push(entry);
  }

  const generated = `/* eslint-disable */
// AUTO-GENERATED by scripts/build-chat-knowledge.mjs — do not edit by hand.
// Re-run: npm run chat:knowledge

export type GeneratedChatService = {
  name: string;
  path: string;
  summary: string;
  details?: string;
};

export const GENERATED_CHAT_SERVICES: GeneratedChatService[] = ${JSON.stringify(services, null, 2)};
`;

  fs.writeFileSync(OUT_FILE, generated, 'utf8');
  console.log(`✓ chat knowledge: ${services.length} services → src/lib/chatKnowledge.generated.ts`);
}

main();
